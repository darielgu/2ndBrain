export function AsciiBrain() {
  const [glitchLine, setGlitchLine] = useState(-1)
  const [scanLine, setScanLine] = useState(0)
  const lines = ASCII_BRAIN.split('\n')

  // Scan line effect
  useEffect(() => {
    const scanInterval = setInterval(() => {
      setScanLine((prev) => (prev + 1) % lines.length)
    }, 50)
    return () => clearInterval(scanInterval)
  }, [lines.length])

  // Random glitch effect
  useEffect(() => {
    const glitchInterval = setInterval(() => {
      if (Math.random() > 0.7) {
        const randomLine = Math.floor(Math.random() * lines.length)
        setGlitchLine(randomLine)
        setTimeout(() => setGlitchLine(-1), 100)
      }
    }, 200)
    return () => clearInterval(glitchInterval)
  }, [lines.length])

  return (
    <div className="relative overflow-hidden">
      {/* Glow effect behind */}
      <div className="absolute inset-0 bg-accent/5 blur-3xl" />
      
      {/* ASCII Art Container */}
      <div className="relative">
        <pre className="text-[3px] leading-[3px] md:text-[4px] md:leading-[4px] lg:text-[5px] lg:leading-[5px] font-mono select-none">
          {lines.map((line, index) => {
            const isScanLine = index === scanLine || index === scanLine + 1
            const isGlitched = index === glitchLine
            
            return (
              <div
                key={index}
                className={`
                  transition-all duration-75
                  ${isScanLine ? 'text-accent brightness-150' : 'text-muted-foreground/40'}
                  ${isGlitched ? 'translate-x-1 text-accent skew-x-2' : ''}
                `}
                style={{
                  opacity: isScanLine ? 1 : 0.4 + (Math.abs(index - scanLine) < 5 ? (5 - Math.abs(index - scanLine)) * 0.1 : 0),
                }}
              >
                {isGlitched ? line.split('').map((char, i) => 
                  Math.random() > 0.8 ? ['#', '%', '@', '*'][Math.floor(Math.random() * 4)] : char
                ).join('') : line}
              </div>
            )
          })}
        </pre>
        
        {/* Scan line overlay */}
        <div 
          className="pointer-events-none absolute left-0 right-0 h-[2px] bg-accent/30 blur-[1px]"
          style={{ 
            top: `${(scanLine / lines.length) * 100}%`,
          }}
        />
        
        {/* CRT flicker overlay */}
        <div className="pointer-events-none absolute inset-0 bg-gradient-to-b from-transparent via-accent/[0.02] to-transparent animate-pulse" />
      </div>
    </div>
  )
}
